package com.lambdaschool.guidedcalculator

class Math {

    fun addIntegers(a: Int, b: Int): Int {
        return a + b
    }

    fun subtractIntegers(a: Int, b: Int): Int {
        return a - b
    }

    fun multiplyIntegers(a: Int, b: Int): Int {
        return a * b
    }

    fun divideIntegers(a: Int, b: Int): Int {
        return a / b
    }

}